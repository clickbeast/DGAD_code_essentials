class BalancedClusterSampler(Sampler):
    """
    Custom sampler for balanced, coverage-guaranteed, and oversampled cluster sampling.
    
    This sampler generates a fixed-length sequence of sample indices for each epoch, with:
      - Coverage guarantee: every sample is seen at least once per epoch.
      - Oversampling: large clusters are sampled more often, according to a tunable probability distribution.
      - Flexible mixing: choose between linear interpolation and exponential (power-law) scaling for cluster probabilities.
      - Fixed epoch size: total samples per epoch = multiplier × dataset size.
      - Reproducible sampling: uses local random generators and seed per epoch.
    
    Sampling methods:
      - LINEAR: Interpolates between original cluster distribution and uniform distribution.
      - EXPONENTIAL: Uses power-law scaling (e.g., Word2Vec negative sampling).
    
    Args:
        dataset: Dataset with cluster information (must have id_to_cluster mapping and ids_in_split).
        sampling_balance (float): Controls balance between original and uniform sampling.
            - 1.0: Original cluster distribution (proportional to cluster sizes)
            - 0.0: Uniform distribution across clusters
            - 0.75: Recommended starting point (Word2Vec standard for exponential)
        sampling_mixing (SamplingMixingMethod): Method for probability calculation.
            - LINEAR: sampling_balance * original + (1-sampling_balance) * uniform
            - EXPONENTIAL: cluster_size^sampling_balance (power-law scaling)
        multiplier (int): Number of times the dataset size to set the epoch length (e.g., 30 for robust coverage).
        seed (int): Random seed for reproducible sampling.
    
    Design notes:
      - Sequence is generated by sampling clusters according to probabilities, then samples within clusters, with replacement.
      - Any missing samples (not picked) are inserted by replacing duplicates, ensuring coverage without changing epoch length.
      - Final sequence is shuffled for randomness and distributed among workers if using DataLoader with num_workers > 1.
    """


    def __init__(self,dataset, sampling_balance=0.75, sampling_mixing=SamplingMixingMethod.LINEAR,multiplier=30,  seed=70):
        super().__init__(dataset)
        self.dataset = dataset
        self.sampling_balance = sampling_balance
        self.sampling_mixing = sampling_mixing
        self.multiplier = multiplier

        self.seed = seed

        print('BalancedClusterSampler: Initializing with:')
        print(f' - sampling_balance: {sampling_balance}')
        print(f' - sampling_mixing: {sampling_mixing}')
        print(f' - multiplier: {multiplier}')
        print(f' - seed: {seed}')

        #This will be the actual epoch size, it will be bigger than the orgina dataset since there is oversampling to account for
        #the cluster distribution based on sample balance while ensuring full coverage of all samples.
        self.epoch_size = len(self.dataset) * self.multiplier

        #Check if sampling_mixing is a string, if so convert to enum
        if isinstance(sampling_mixing, str):
            sampling_mixing_str_to_enum = { 
                'linear': SamplingMixingMethod.LINEAR,
                'exponential': SamplingMixingMethod.EXPONENTIAL
            }
            self.sampling_mixing = sampling_mixing_str_to_enum.get(sampling_mixing.lower())


        # Build cluster mapping  'clusteris': [0, 1, 3, 7 ...], the elements is the idx of the ids_in_split
        self.cluster_to_indices = {}
        for idx, sample_id in enumerate(dataset.ids_in_split):
            cluster_id = dataset.id_to_cluster[sample_id]
            if cluster_id not in self.cluster_to_indices:
                self.cluster_to_indices[cluster_id] = []
            self.cluster_to_indices[cluster_id].append(idx)
        
        self.unique_clusters = list(self.cluster_to_indices.keys())
        if self.sampling_mixing == SamplingMixingMethod.LINEAR:
            self._calculate_probabilities_for_linear()
        elif self.sampling_mixing == SamplingMixingMethod.EXPONENTIAL:
            self._calculate_probabilities_for_exponential()


    def _calculate_probabilities_for_linear(self):
        """Calculate cluster probabilities based on the sampling balance."""
        cluster_sizes = np.array([len(self.cluster_to_indices[cluster_id]) for cluster_id in self.unique_clusters])
        original_probs = cluster_sizes / cluster_sizes.sum()
        flat_probs = np.ones(len(self.unique_clusters)) / len(self.unique_clusters)
        self.cluster_probs = self.sampling_balance * original_probs + (1-self.sampling_balance) * flat_probs

    def _calculate_probabilities_for_exponential(self):
        cluster_sizes = np.array([len(self.cluster_to_indices[cluster_id]) for cluster_id in self.unique_clusters])
        powered_sizes = np.power(cluster_sizes, self.sampling_balance)
        self.cluster_probs = powered_sizes / np.sum(powered_sizes)
        pass
    
    def __iter__(self):
        # Epoch-based seeding (crucial for different orders each epoch)
        rng_py = random.Random((self.seed or 0) + self.epoch)
        rng_np = np.random.default_rng((self.seed or 0) + self.epoch)
        #np.random.seed((self.seed or 0) + self.epoch)

        # Generate complete balanced sequence
        full_sequence = self._generate_balanced_sequence(rng_py, rng_np)
        
        #No need to do splitting logic here, since the DataLoader will handle it.
        # Auto-split among workers (no worker-specific logic needed!)
        #worker_info = torch.utils.data.get_worker_info()
        #print(worker_info)
        # if worker_info is None:
        #     return iter(full_sequence)
        # else:
        #     worker_id = worker_info.id
        #     num_workers = worker_info.num_workers
        #     worker_indices = full_sequence[worker_id::num_workers]
        #     return iter(worker_indices)

        return iter(full_sequence)

    def _generate_balanced_sequence(self, rng_py, rng_np): 
        """Generate fixed-length balanced sequence with coverage guarantee."""

        # Step 1: Sample clusters according to probabilities, with replacement

        print('Picking clusters')
        chosen_clusters = rng_np.choice(self.unique_clusters, 
                                   size=self.epoch_size, 
                                   p=self.cluster_probs)

        sampled_indices = []
        for chosen_cluster_id in tqdm(chosen_clusters, desc='generating balanced sequence'):
            #chosen_cluster_id = rng_np.choice(self.unique_clusters, p=self.cluster_probs)
            # Pick a random sample from the chosen cluster (with replacement)
            sample_idx = rng_np.choice(self.cluster_to_indices[chosen_cluster_id])
            sampled_indices.append(sample_idx)

        # Step 2: Coverage guarantee - find missing samples
        all_indices = set(range(len(self.dataset)))
        seen = set(sampled_indices)
        missing = list(all_indices - seen)

        # Step 3: Replace duplicates with missing samples
        counts = Counter(sampled_indices)
        replace_positions = [i for i, idx in enumerate(sampled_indices) if counts[idx] > 1]
        for miss_idx, pos in zip(missing, replace_positions):
            counts[sampled_indices[pos]] -= 1
            sampled_indices[pos] = miss_idx
            counts[miss_idx] += 1

        # Step 4: Shuffle for randomness
        rng_py.shuffle(sampled_indices)
        return sampled_indices



    def _generate_balanced_sequence_old(self): 
        """Generate complete balanced sequence with coverage guarantee."""

        #Create a copu of the clusters
        cluster_pools = {cluster_id: list(indices) for cluster_id, indices in self.cluster_to_indices.items()}
        
        # Shuffle within clusters
        for pool in cluster_pools.values():
            random.shuffle(pool)
        
        result_indices = []
        remaining = {cluster_id: len(pool) for cluster_id, pool in cluster_pools.items()}
        
        while sum(remaining.values()) > 0:
            
            # Available cluster ids
            available = [cluster_id for cluster_id in self.unique_clusters if remaining[cluster_id] > 0]
            if not available:
                break
            
            # Probabilities for available clusters
            probs = [self.cluster_probs[self.unique_clusters.index(cluster_id)] for cluster_id in available]
            #Renormalize probabilities
            probs = np.array(probs) / sum(probs)
            
            # Choose cluster and sample
            chosen_cluster_id = np.random.choice(available, p=probs)
            sample_idx = cluster_pools[chosen_cluster_id].pop()
            result_indices.append(sample_idx)
            remaining[chosen_cluster_id] -= 1

        # Return the balanced sequence of indices this index is ussed to sample from the dataset
        return result_indices




            
    def set_epoch(self, epoch):
        """MUST call this at start of each epoch!"""
        self.epoch = epoch


    def __len__(self):
        return self.epoch_size
    



