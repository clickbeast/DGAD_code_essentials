class Tracker:
    

    """
    Tracks training progress for step-based or epoch-based training loops.

    This class wraps a PyTorch DataLoader and provides:
      - Automatic tracking of steps, epochs, batches, and total samples seen.
      - Early stopping via the `self._stop_training` flag.
      - Progress printing with batch and step counters.
      - Flexible stopping: either after a fixed number of steps (`n_steps`) or epochs (`n_epochs`).
      - Handles both full and partial batches via the `drop_last` argument.

    Design notes:
      - Counters (`step`, `batch_in_epoch`, etc.) are incremented *before* yielding each batch,
        so progress reflects the batch currently being processed (1-based indexing).
      - The loop will always stop exactly at the requested number of steps or epochs, even if extra
        batches/epochs are available, thanks to internal checks and a safety buffer for step-based mode.
      - Explicit early stopping is supported by setting `self._stop_training = True` from anywhere.

    Args:
        dataloader (DataLoader): The PyTorch DataLoader to wrap.
        sampler (Sampler): The sampler used for epoch control.
        n_steps (int, optional): Stop after this many steps (batches). Mutually exclusive with n_epochs.
        n_epochs (int, optional): Stop after this many epochs. Mutually exclusive with n_steps.
        drop_last (bool): If True, only count full batches per epoch.

    Example:
        tracker = Tracker(dataloader, sampler, n_steps=1000)
        for epoch in tracker.epochs():
            for batch in tracker():
                # Training code...
                tracker.print_progress()
    """


    def __init__(self, dataloader, sampler, n_steps=None, n_epochs=None, drop_last=False):
        if n_steps is None and n_epochs is None:
            raise ValueError("Must specify either n_steps or n_epochs")
        if n_steps is not None and n_epochs is not None:
            raise ValueError("Specify only one: n_steps or n_epochs")
        
        self.dataloader = dataloader
        self.sampler = sampler
        self.n_steps = n_steps
        self.n_epochs = n_epochs
        self.drop_last = drop_last
        
        # Calculate dataset info
        epoch_size = len(sampler)
        batch_size = dataloader.batch_size
        self.batch_size = batch_size
        if drop_last:
            self.batches_per_epoch = epoch_size // batch_size
        else:
            self.batches_per_epoch = (epoch_size + batch_size - 1) // batch_size
        
        # Calculate max epochs needed
        if n_steps is not None:
            #with safety batch
            self.max_epochs = (n_steps // self.batches_per_epoch) + 2
            #precise
            #self.max_epochs = (n_steps + self.batches_per_epoch - 1) // self.batches_per_epoch
        else:
            self.max_epochs = n_epochs
        
        # Tracking state
        self.step = -1 # Start at -1 to make first batch 0 idx based, increment happens before yielding. 
        self.epoch = 0 
        self.total_samples_seen = 0
        self.batch_in_epoch = -1  # Start at -1 to make first batch 0 idx based, increment happens before yielding.
        self._stop_training = False
        self.avg_val_loss = None  # Placeholder for average validation loss if needed
        

    def resume(self, step, epoch, complete_epoch=True):
        #Raise if not starting from a complete epoch
        if complete_epoch:
            print("Tracker: Resuming from a complete epoch.")
            self.step = step
            self.epoch = epoch + 1  # <-- Start from the next epoch after the last completed one
        else:
            print('Tracker: Resuming from a non-complete epoch.')
            raise ValueError("Cannot resume from a non-complete epoch. Not implemented yet.")

    def epochs(self, fallback_max=999):
        """Iterator for epochs - uses calculated max or fallback"""
        max_epochs = self.max_epochs if hasattr(self, 'max_epochs') else fallback_max
        
        for epoch_num in range(self.epoch, max_epochs):
            if self._stop_training:
                break
                
            self.epoch = epoch_num
            self.batch_in_epoch = 0
            self.sampler.set_epoch(epoch_num)
            
            yield epoch_num
            
            # Stop if epoch-based and we've completed target epochs
            #if self.n_epochs is not None and epoch_num + 1 >= self.n_epochs:
            #   break
    
    def __call__(self, dataloader=None):
        """Make tracker callable to wrap dataloader"""
        dl = dataloader or self.dataloader
        
        for batch in dl:

            if self._stop_training:
                print("Tracker: Stopping training due to early stop flag.")
                break
                
            
            self.total_samples_seen += len(batch['pdb_id'])  # Assuming batch has pdb_id
            self.step += 1
            self.batch_in_epoch += 1

            yield batch

            
            # Check if we should stop (step-based)
            if self.n_steps is not None and self.step >= self.n_steps:
                self._stop_training = True
                break
    
    def print_progress(self, info=None):
        """Print current progress"""
        if self.n_steps:
            step_progress = f"Step {self.step+1}/{self.n_steps} ({100*(self.step+1)/self.n_steps:.1f}%)"
        else:
            step_progress = f"Step {self.step}"

        if hasattr(self, 'max_epochs') and self.max_epochs:
            epoch_progress = f"Epoch {self.epoch+1}/{self.max_epochs} ({100*(self.epoch+1)/self.max_epochs:.1f}%)"
        else:
            epoch_progress = f"Epoch {self.epoch}"

        print(f"{step_progress} | {epoch_progress}, Batch {self.batch_in_epoch + 1}/{self.batches_per_epoch} | "
              f"Samples seen: {self.total_samples_seen}")
        
        if info is not None:
            print(info)


    def get_progress(self):
        """Return current progress statistics as a dict for logging/monitoring."""
        progress = {
            'step': self.step,
            'steps_total': self.n_steps if self.n_steps else None,
            'step_percent': (100 * (self.step + 1) / self.n_steps) if self.n_steps else None,
            'epoch': self.epoch,  # 1-based for reporting
            'epochs_total': self.max_epochs if hasattr(self, 'max_epochs') else None,
            'epoch_percent': (100 * (self.epoch + 1) / self.max_epochs) if hasattr(self, 'max_epochs') and self.max_epochs else None,
            'batch_in_epoch': self.batch_in_epoch,
            'batches_per_epoch': self.batches_per_epoch,
            'batch_percent': (100 * (self.batch_in_epoch + 1) / self.batches_per_epoch) if self.batches_per_epoch else None,
            'samples_seen': self.total_samples_seen
        }
        return progress
    
    def save_checkpoint(self, model, optimizer, scheduler, config, ckpt_path, complete_epoch=False, extra=None):
            """
            Save a training checkpoint with all relevant state.
            Args:
                model: The model to save (should have .state_dict()).
                optimizer: The optimizer to save (should have .state_dict()).
                scheduler: The scheduler to save (should have .state_dict()).
                config: The config object/dict to save.
                ckpt_path: Path to save the checkpoint file.
                extra: Optional dict of extra fields to include in the checkpoint.
            """
            state = {
                'config': config,
                'model': model.state_dict(),
                'optimizer': optimizer.state_dict(),
                'scheduler': scheduler.state_dict(),
                'iteration': self.step,
                'step': self.step,
                'epoch': self.epoch,
                'tracker_progress': self.get_progress(),
                'complete_epoch': complete_epoch,
                'avg_val_loss': getattr(self, 'avg_val_loss', None),
            }
            if extra:
                state.update(extra)
            import torch
            torch.save(state, ckpt_path)

