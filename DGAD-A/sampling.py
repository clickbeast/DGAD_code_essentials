def check_epoch_similarity(sampler, dataloader):
    """
    Check if the sampler generates different sequences for multiple epochs.
    Prints the sequences for the first few epochs to verify diversity.
    """
    num_epochs = 3
    sequences = []
    for epoch in range(num_epochs):
        sampler.set_epoch(epoch)
        sequence = list(iter(sampler))
        sequences.append(sequence)
        print(f"Epoch {epoch}: First 10 indices: {sequence[:10]}")
        print(f"Epoch {epoch}: Last 10 indices: {sequence[-10:]}")
        print(f"Epoch {epoch}: Sequence length: {len(sequence)}\n")

    # Compare sequences for diversity
    for i in range(num_epochs - 1):
        overlap = len(set(sequences[i]) & set(sequences[i+1]))
        print(f"Overlap between epoch {i} and {i+1}: {overlap} samples")



